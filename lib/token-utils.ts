/**
 * 根据字符数粗略估算 token 数量。
 *
 * 使用 4 字符 ≈ 1 token 的经验规则，适用于英文为主的文本。
 * 对于中文等宽字符语言可能偏低估。
 *
 * @param chars - 字符数
 * @returns 估算的 token 数量
 */
export function estimateTokens(chars: number): number {
  return Math.round(chars / 4);
}
