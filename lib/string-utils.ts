/**
 * 按逗号拆分字符串并清理空白。
 *
 * 适用于解析配置中的逗号分隔列表（如工具列表、模型 ID 列表）。
 *
 * @param s - 逗号分隔的字符串（如 "read, bash, edit"）
 * @returns 清理后的字符串数组，过滤空值
 */
export function parseCommaList(s: string | undefined): string[] {
  return s
    ?.split(",")
    .map((t) => t.trim())
    .filter(Boolean) ?? [];
}
